package org.gk.layout;

/**
 * X and y coordinates of a point.
 * 
 * @author David Croft
 *
 */
public class Point {
	public double x;
	public double y;
	
	public Point(double x, double y) {
		this.x = x;
		this.y = y;
	}
}
